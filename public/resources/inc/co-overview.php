    <p>EOG Resources, Inc., together with its subsidiaries, engages in the exploration, development, production, and marketing of crude oil and natural gas primarily in the United States, Canada, the Republic of Trinidad and Tobago, the United Kingdom, the People's Republic of China, and the Argentine Republic. As of December 31, 2011, it had a total estimated net proved reserves of 2,054 million barrels of oil equivalent (MMBoe), of which 517 million barrels (MMBbl) were crude oil and condensate reserves, and 228 MMBbl were natural gas liquids reserves; and 7,851 billion cubic feet were natural gas reserves. The company held approximately 572,000 net acreage position in the oil window of the Eagle Ford Shale Play near San Antonio, Texas. EOG Resources, Inc. was founded in 1985 and is headquartered in Houston, Texas.</p>

